def parse_reanim_file(reanim_file):
    images = []
    result = subprocess.run(
        [str(AnimationReaderExecutable), str(reanim_file)],
        capture_output=True,
        text=True
    )
    if result.returncode != 0:
        print(f"Error processing {reanim_file}: {result.stderr}")
        return []
    
    for line in result.stdout.splitlines():
        print(line)
        if line.startswith(" >> "):
            images.append(line[4:].strip())

    # Assume the output is a list of image filenames, one per line
    return images

allocated_resources = []

def find_in_resources(resources: List[Path], image_const_name: str):
    name = image_const_name
    if name.startswith("IMAGE_REANIM_"):
        name = name[13:].lower()

    for resource in resources:
        if name in Path(resource).name.lower() and resource not in allocated_resources:
            if Path(resource).stem.lower() in name:
                allocated_resources.append(resource)
                return resource
    return None

